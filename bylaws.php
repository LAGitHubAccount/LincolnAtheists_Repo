                    <header>
                        <h2>Lincoln Atheists Bylaws</h2>
                        <p>Approved March 18, 2015</p>
                    </header>
                    <section>
                        <header>
                            <h3>Mission Statement</h3>
                        </header>
                        <p>
                            Lincoln Atheists is a non-profit educational organization dedicated to building a positive community for non-theists; fostering secular viewpoints; supporting charitable contributions to society; protecting and promoting the separation of church and state.
                        </p>
                    </section>
                    <section>
                        <header>
                            <h3>Article 1: Organization</h3>
                        </header>
                        <p>
                            1.1 Name - The name of this organization is Lincoln Atheists.
                        </p>
                        <p>
                            1.2 Purpose - Lincoln Atheists is organized as a nonprofit educational corporation to develop and support the
                            atheist, rationalist, secular humanist, agnostic, skeptic and non-theist communities; to provide 
                            opportunities for socializing and friendship among these groups; to promote and defend their views; to 
                            protect the first amendment principle of state-church separation; to oppose any discrimination based upon 
                            religious conviction, particularly when it is directed at the non-religious; to expose the dangers of 
                            supernaturalism and superstition; to promote science; and to work with other organizations in pursuit of 
                            common goals.
                            This organization shall operate in an open, democratic manner, without discrimination as to gender, race, 
                            age, sexual orientation, religious affiliation, ethnic origin, nationality or disability.
                        </p>
                        <p>
                            1.3 Non-profit Status - Lincoln Atheists shall operate exclusively as a nonprofit corporation pursuant to the 
                            “Nebraska Non-profit Corporation Act” of the State of Nebraska and Section 501(c)(3) of the Internal 
                            Revenue Code of the United States of America.
                        </p>
                        <p>
                            1.4 Annual Accounting Period - The fiscal year begins on January 1st and ends on December 31st.
                        </p>
                        <p>
                            1.5 Records - Records shall be kept of all the activities and transactions of Lincoln Atheists. All documents 
                            will be kept at the office of the Corporation and shall include copies of the Articles of Incorporation, 
                            Certificate of Incorporation, these Bylaws, minutes of meetings of the Board of Directors (hereafter referred 
                            to as “the Board”) and members meetings, and a record of all financial transactions.
                        </p>
                        <p>
                            1.6 Finances - All unemployed funds of Lincoln Atheists shall be deposited in bank accounts, trust 
                            companies, or other depositories as determined by the Board. Maintenance, deposit, and withdrawal from 
                            these accounts will be the responsibility of the Treasurer or an alternate agent as determined by the Board. 
                            Lincoln Atheists’ funds shall be in accounts structured as follows:</p>
                        <p>
                            1.6.1 Checking Account - The purpose of the checking account is to support the normal, day-to-day
                            activities of Lincoln Atheists. Normal inflows will include, but are not limited to, membership 
                            dues and anonymous donations made at meetings. The Treasurer is authorized to pay routine 
                            expenditures out of this account.
                        </p>
                        <p>
                            1.6.2 Permanent Fund - The purpose of the permanent fund is to establish an eventual, long-term
                            source of income and emergency cash to ensure Lincoln Atheists’ survival. Normal inflows are 
                            to include any donation earmarked for the permanent fund, and any other moneys that Lincoln 
                            Atheists’ Board choose to deposit there. Withdrawals from the permanent fund may only occur 
                            with the approval of seventy percent (70%) or greater of the Board members.</p>
                    </section>
                    <section>
                        <header>
                            <h3>Article 2: Members</h3>
                        </header>
                        <p>
                            2.1 Membership - Membership in Lincoln Atheists is open to anyone who desires to support the need to 
                            educate the general public on the meaning and validity of atheism; who seeks to promote reason, science 
                            and critical thinking; or who supports the principles of church-state separation.</p>

                        <p> 2.2 Application for Membership - Anyone seeking membership in the organization shall provide their name 
                            and address, phone number or email address, and pay their annual dues. Lincoln Atheists will keep 
                            accurate records of its membership. The membership roll of Lincoln Atheists is strictly confidential, no 
                            member's identification is to be made public without the expressed permission of that member.</p>
                        <p> 2.3 Termination of Membership - Any member can resign their membership at any time for any reason. The 
                            Board may terminate an individual's membership by letter, or email, stating the reasons and giving at least 
                            thirty (30) days advance notice of the effective date of the termination. The member shall have a chance to 
                            be heard either orally or in writing before termination takes effect. Malicious or threatening behavior by 
                            any member, as determined by the Board, will result in immediate termination of membership with no 
                            advanced notice. Membership dues will not be refunded for resignation or termination of membership.</p>
                        <p> 2.4 Membership Dues - Membership Dues shall be reviewed at least once per year, and may be changed 
                            subject to approval by vote of the Board.
                        </p>
                    </section>
                    <section>
                        <header>
                            <h3>Article 3: Board of Directors</h3>
                        </header>
                        <p>
                            3.1 Composition of the Board - The Board shall number seven (7) members nominated from, and elected by,
                            the membership of Lincoln Atheists.
                        </p>
                        <p>
                            3.1.1 President - Serves as the chief executive officer of Lincoln Atheists; presides over each meeting
                            of the Board or designates another Board member to preside. Facilitates cooperation between 
                            Board members and their committees to ensure goals and duties of all committees are 
                            completed efficiently and effectively. Acts as the public face of the group in the community and 
                            to news media outlets, or designates another member to do so. Ensures that Board members 
                            are fulfilling their appointed duties.
                        </p>
                        <p>
                            3.1.2 Secretary - Has custody of and maintains the Articles of Incorporation, the Bylaws, and other
                            basic records relating thereto. Oversees membership records and member benefits. Maintains 
                            minutes of Board meetings, and other records as instructed by the President.
                        </p>
                        <p>
                            3.1.3 Treasurer - Accounts for all funds of Lincoln Atheists, as required by law and by orders of the
                            Board. Deposits Lincoln Atheists’ funds in a safe depository approved by the Board. Co-chairs 
                            the Fundraising Committee. Prepares financial and other reports required by governmental
                        </p>
                        <p>
                            3.1.4 Director of Education - Presides over meetings of the Education Committee. Coordinates
                            education opportunities for members and non-members and develops educational outreach 
                            programs for the general public. Maintains a lending library to be made available to members in 
                            good standing.
                        </p>
                        <p>
                            3.1.5 Director of Community Service - Presides over meetings of the Community Service
                            Committee. Coordinates community service projects and charitable giving for members and 
                            non-members. Organizes and implements community service projects and philanthropic 
                            endeavors.
                        </p>
                        <p>
                            3.1.6 Director of Marketing - Presides over meetings of the Marketing Committee. Coordinates
                            marketing, advertising and promotional activities via print and digital media outlets. Expands 
                            membership in the organization. Co-chairs the Fundraising Committee. Assists designated 
                            spokespersons with presenting cohesive messages about Lincoln Atheists to the media, general 
                            public, or public officials on behalf of the organization.</p>
                        <p>
                            3.1.7 Director of Activities - Presides over meetings of the Activities Committee. Organizes and 
                            promotes monthly gatherings and additional activities for all demographics. Coordinates 
                            activism and response to social and community issues. Confers with the Board prior to 
                            scheduling activism events.</p>
                        <p>
                            3.2 Board Elections - Board elections will be held at one of the regular member meetings during the fourth 
                            quarter of each year. Nominations are open to any Lincoln Atheists member in good standing. Elections 
                            will be determined by ballot at the meeting, with provision made for absentee ballots for any Lincoln
                            Atheists member who cannot attend the meeting in person.
                        </p>
                        <p>
                            3.3 Board Term of Office - The term of directorship shall be for a two year period with a portion of Board 
                            positions being elected on alternating years. The positions of President, Director of Activities, and Director 
                            of Marketing shall be elected on even-numbered years. The positions of Secretary, Treasurer, Director of 
                            Community Service and Director of Education shall be elected on odd-numbered years. Directors may run 
                            for re-election. In the event of a resignation, the vacancy shall be filled by Board appointment. Any 
                            director may be removed for cause by the majority vote of the Board or the general membership. </p>
                        <p>
                            3.4 Powers and Responsibilities of the Board - The Board is the primary policy and decision-making body 
                            of Lincoln Atheists. The Directors are responsible for carrying out the goals and objectives of Lincoln 
                            Atheists as set forth in these Bylaws and the Articles of Incorporation. The Board shall determine what 
                            program of activities will be undertaken by Lincoln Atheists.</p>
                        <p>
                            3.4.1 The Board shall propose a budget for each fiscal year. The budget shall describe the activity
                            program planned for the year and estimate the required expenses to implement the program. 
                            The budget shall state how the expenditure program will be financed.
                        </p>
                        <p> 
                            3.4.2 After the close of each fiscal year, the Board shall present a report covering the activities of the
                            organization for the past year including the assets and liabilities, revenue or receipts, and 
                            expenditures of the organization.
                        </p>
                        <p>
                            3.4.3 The Board may take emergency action to respond to events in a way that supports our
                            organizational purposes if there is insufficient time for the membership to be notified and vote at 
                            a member meeting.
                        </p>
                        <p>
                            3.4.4 The Board shall review and approve applications for membership and may terminate
                            membership for cause.
                        </p>
                        <p>
                            3.4.5 No person shall communicate with the media, general public, or public officials on behalf of
                            Lincoln Atheists who has not been authorized to do so by a majority of the Board. Therefore, the 
                            Board shall appoint one or more official spokespersons.
                        </p>
                        <p>
                            3.4.6 Any financial transaction over one hundred dollars ($100) shall be approved by the Board in
                            writing. The voting members of the Board will be composed of individuals who do not have a 
                            conflict of interest concerning the transaction.
                        </p>
                        <p>
                            3.5 The Directors shall receive no compensation for acting as such and no Director shall directly or indirectly 
                            receive any profit from his/her position. Directors may receive reasonable compensation for expenses 
                            incurred by them in the normal course of their duties as approved by the Board.
                        </p>
                    </section>
                    <section>
                        <header>
                            <h3>Article 4: Meetings</h3>
                        </header>
                        <p>
                            4.1 Member Meetings - Member meetings shall be held monthly unless determined otherwise by the Board.
                            Meetings will occur at a place and time determined by the Board. Special meetings may be called by any 
                            two (2) of the Directors.
                        </p>
                        <p>
                            4.1.2 Notice of Meetings - Members shall be notified of all regular or special member meetings at 
                            least seven (7) days before the date of the meeting. Notification includes notification by mail, 
                            telephone, facsimile machine, website posting or e-mail. The notice of any meeting shall include 
                            the agenda and any matters to be voted on. Members may vote by absentee ballot.
                        </p>
                        <p>
                            4.2 Board Meetings - The Board shall hold regular monthly meetings unless determined otherwise by
                            the Board. Additional meetings may be held, as scheduled by request of any two (2) Directors. 
                            Such meetings may be held "in person"; by mail; by telephone or internet conference call; or by e-
                            mail.
                        </p>
                        <p>
                            4.3 Quorum - A quorum is required to conduct any appropriate business.
                        </p>
                        <p>
                            4.3.1 Quorum for Board of Directors - A majority of the total number of Directors constitutes a
                            quorum.
                        </p>
                        <p>
                            4.3.2 Quorum for Member Meetings - A quorum shall consist of at least ten (10) physically present
                            voting members of the organization.
                        </P>
                        <p>
                            4.4 Meeting Agendas - Meeting agendas should be prepared by the Secretary and distributed electronically
                            one week before each meeting. Any member in good standing may request an agenda item. Any member 
                            may ask that an item be added to the agenda or make a motion that an item be put to a vote. If there is 
                            insufficient time at a meeting for the new business to be transacted then it will be placed on the agenda for 
                            the next regular meeting.
                        </p>
                    </section>
                    <section>
                        <header>
                            <h3>Article 5: Change to Bylaws</h3>
                        </header>
                        <p>
                            5.1 Changes to Bylaws - Changes to the Bylaws should only be made to improve the operation of Lincoln
                            Atheists. Under no circumstances shall they alter the purpose of this organization as set forth in Article 1. 
                            The following procedure shall be used to make changes to the Bylaws:
                        </p>
                        <p>
                            5.1.1 Proposing Changes - Any proposed amendments, additions or deletions to the Bylaws shall be 
                            proposed at a regularly scheduled member meeting, either by the Board or a member.
                        </p>
                        <p>
                            5.1.2 Publicizing Proposed Changes - The proposed changes shall be included in the meeting
                            notice for the next regularly scheduled Board meeting and noted as an agenda item. The 
                            proposed changes may be debated and revised before being voted upon.
                        </p>
                        <p>
                            5.1.3 Voting on Proposed Changes - Changes to the Bylaws must be approved by a voting majority
                            of Board members.
                        </p>
                        <p>
                            5.2 Adoption - Changes to the Bylaws shall become effective immediately upon adoption, unless specified
                            otherwise in the motion to adopt.
                        </p>
                        <p>
                            5.3 Technical Amendments - A “technical amendment” is a change in the Bylaws which affects only spelling,
                            grammar, punctuation, parallel structure, consistent usage, or the numbering or titling of the various parts 
                            of the Bylaws or cross-references thereto.
                        </p>
                        <p>
                            5.3.1 Titles - The numbers and titles of the various parts of the Bylaws are for ease of reference only
                            and have no substance or effect.
                        </p>
                        <p>
                            5.3.2 Secretary’s Power - The Secretary may initiate any technical amendment by presenting it in
                            writing at any Board meeting. 
                        </p>
                        <p>
                            5.3.3 Board Veto - The Board may, by majority vote, reject any technical amendment proposed by the
                            Secretary. Any such proposal not rejected by the Board shall take effect at the end of the 
                            meeting at which it was presented. 
                        </p>
                    </section>
                    <section>
                        <header>
                            <h3>Article 6: Dissolution and Distribution of Assets</h3>
                        </header>
                        <p>
                            6.1 Dissolution - Should the Lincoln Atheists be dissolved, any assets of the organization will be used only to 
                            further the purpose of the organization. All outstanding debts, obligations and claims must be satisfied 
                            before assets are distributed. No assets shall be distributed to individuals.
                        </p>
                    </section>
