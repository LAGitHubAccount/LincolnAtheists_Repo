                        <h2>History</h2>
                        <p>How Lincoln Atheists came to be.</p>
                    </header>
                    <p>
                        Lincoln Atheists was started solely as a social group for likeminded people in 2007 by Brian Aden (current President). Brian used his own time, money, and effort to give atheists in Lincoln an outlet in a notoriously conservative and religious state. The group initially used Meetup.org as a means to attract members and publicize social events, such as Coffee and Conversation and Monthly Dinner meetups. LA also made a few excursions to Ashfall Fossil Beds, and attended many movies and scientific lectures in the area.</p>
                    <p>
                        In March of 2015, it was collectively decided by a majority of members to incorporate Lincoln Atheists as a 501(c)(3) non-profit organization, whereby LA could raise tax-free funds in order to promote the Mission Statement and broaden the scope of LA’s influence in Lincoln’s secular advocacy community.</p>
                    <p>In order to achieve the goals of the Mission Statement, LA created Education, Community Service, Activities, Marketing, and Fundraising Committees after adoption of By-Laws and Articles of Incorporation in April of 2015. Committees continue to generally meet on a monthly schedule and are open to the all official members as well as members of the public.</p>

